//JS String Method | startsWith()

// The startsWith() method returns true if a string starts with a specified string.

// Otherwise it returns false.

// The startsWith() method is case sensitive.

// Starts With Position 0
let text = "Hello world, welcome to the universe.";
let result = text.startsWith("Hello");
console.log(result);

// Starts With Position 6 
let text1 = "Hello world, welcome to the universe.";
let result1 = text.startsWith("world",6);
console.log(result1);

